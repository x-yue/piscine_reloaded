#include <stdio.h>

int		ft_sqrt(int nb)
{
	long i;

	i = 1;
	while ((nb > 0) && (i < (nb / 2)))
	{
		if (i * i == nb)
			return (i);
		if (i * i > nb)
			return (0);
		else
			i++;
	}
	return (0);
}
